        user = update.effective_user
        logger.info(f"–ü–æ–ª—É—á–µ–Ω–æ –∏–º—è –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user.id}: {update.message.text}")
        
